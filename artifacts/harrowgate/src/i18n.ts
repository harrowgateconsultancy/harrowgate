export function useLang() {
  const [lang, setLangState] = useState<LangCode>(() => {
    try { return (localStorage.getItem("hg-lang") as LangCode) || "en"; } catch { return "en"; }
  });

  const setLang = (l: LangCode) => {
    try { localStorage.setItem("hg-lang", l); } catch { /* */ }
    setLangState(l);
  };

  const t = (key: string): string => {
    const val = T[lang]?.[key] ?? T.en[key] ?? key;
    return val;
  };

  const isRtl = LANG_LIST.find(l => l.code === lang)?.rtl ?? false;

  useEffect(() => {
    document.documentElement.setAttribute("dir", isRtl ? "rtl" : "ltr");
  }, [isRtl]);

  return { lang, setLang, t, isRtl, LANG_LIST };
}
